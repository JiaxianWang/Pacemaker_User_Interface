    private void jButton_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registerActionPerformed
        // get data from fields
         String username = jTextField_name.getText();
         String pass1 = String.valueOf(jPasswordField_password.getPassword());
         String pass2 = String.valueOf(jPasswordField_confirmpassword.getPassword());
         
         
        
// check if the data are empty
         if(verifyFields())
         {
// check if the username already exists
             if(!checkUsername())
             {
                 
                 pref.put(username,pass1);
                 JOptionPane.showMessageDialog(null, "Account registered go back to login page","Confirm Password",2);
                 
                 
             }else{JOptionPane.showMessageDialog(null, "The account if full, fail to register","Confirm Password",2);}
         }
    }//GEN-LAST:event_jButton_registerActionPerformed

