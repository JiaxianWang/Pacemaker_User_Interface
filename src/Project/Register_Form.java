    public boolean verifyFields()
    {

        String uname = jTextField_name.getText();

        String pass1 = String.valueOf(jPasswordField_password.getPassword());
        String pass2 = String.valueOf(jPasswordField_confirmpassword.getPassword());
        
        // check empty fields
        if(uname.trim().equals("") || pass1.trim().equals("") || pass2.trim().equals(""))
        {
            JOptionPane.showMessageDialog(null, "One Or More Fields Are Empty","Empty Fields",2);
            return false;
        }
        
        // check if the two password are equals or not
        else if(!pass1.equals(pass2))
        {
           JOptionPane.showMessageDialog(null, "Password Doesn't Match","Confirm Password",2); 
           return false;
        }
        
        // if everything is ok
        else{
            return true;
        }
    }
    
    // create a function to check if the entered username already exists in the database
    public boolean checkUsername(){
        
        //if the account is more than 10 , then fail to join 
        boolean fail = false;
        try {
            user_count=Array.getLength(pref.keys());
        } catch (BackingStoreException ex) {
            Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(user_count>10){
            fail=true;
            JOptionPane.showMessageDialog(null, "Fail to register due to reach the maximum user"," Error",2); 
        }
        
        
        
        return fail;
    }
    
    
    
    
